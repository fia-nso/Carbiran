alter table public.profiles enable row level security;
alter table public.vehicules enable row level security;
alter table public.ravitaillements_vehicules enable row level security;
alter table public.activity_logs enable row level security;

drop policy if exists "profiles_select_own_or_manager" on public.profiles;
create policy "profiles_select_own_or_manager"
on public.profiles
for select
to authenticated
using (
  id = auth.uid()
  or public.is_manager_or_admin()
);

drop policy if exists "profiles_insert_admin_only" on public.profiles;
create policy "profiles_insert_admin_only"
on public.profiles
for insert
to authenticated
with check (
  public.is_admin()
);

drop policy if exists "profiles_update_self_or_admin" on public.profiles;
create policy "profiles_update_self_or_admin"
on public.profiles
for update
to authenticated
using (
  id = auth.uid()
  or public.is_admin()
)
with check (
  id = auth.uid()
  or public.is_admin()
);

drop policy if exists "profiles_delete_admin_only" on public.profiles;
create policy "profiles_delete_admin_only"
on public.profiles
for delete
to authenticated
using (
  public.is_admin()
);

drop policy if exists "vehicules_select_all_authenticated" on public.vehicules;
create policy "vehicules_select_all_authenticated"
on public.vehicules
for select
to authenticated
using (true);

drop policy if exists "vehicules_write_manager_or_admin" on public.vehicules;
create policy "vehicules_write_manager_or_admin"
on public.vehicules
for all
to authenticated
using (public.is_manager_or_admin())
with check (public.is_manager_or_admin());

drop policy if exists "ravitaillements_select_all_authenticated" on public.ravitaillements_vehicules;
create policy "ravitaillements_select_all_authenticated"
on public.ravitaillements_vehicules
for select
to authenticated
using (true);

drop policy if exists "ravitaillements_write_manager_or_admin" on public.ravitaillements_vehicules;
create policy "ravitaillements_write_manager_or_admin"
on public.ravitaillements_vehicules
for all
to authenticated
using (public.is_manager_or_admin())
with check (public.is_manager_or_admin());

drop policy if exists "activity_logs_admin_select" on public.activity_logs;
create policy "activity_logs_admin_select"
on public.activity_logs
for select
to authenticated
using (public.is_admin());

drop policy if exists "activity_logs_insert_own" on public.activity_logs;
create policy "activity_logs_insert_own"
on public.activity_logs
for insert
to authenticated
